package org.ooad.facebook_v01.model;

public class Places {
	private String places_current_city;
	private String places_hometown;
	private int userdetails_pk;
	private int places_pk;
	public Places(){
		
	}
	public String getPlaces_current_city() {
		return places_current_city;
	}
	public void setPlaces_current_city(String places_current_city) {
		this.places_current_city = places_current_city;
	}
	public String getPlaces_hometown() {
		return places_hometown;
	}
	public void setPlaces_hometown(String places_hometown) {
		this.places_hometown = places_hometown;
	}
	public int getUserdetails_pk() {
		return userdetails_pk;
	}
	public void setUserdetails_pk(int userdetails_pk) {
		this.userdetails_pk = userdetails_pk;
	}
	public int getPlaces_pk() {
		return places_pk;
	}
	public void setPlaces_pk(int places_pk) {
		this.places_pk = places_pk;
	}
	
	
	

}
